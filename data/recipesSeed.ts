import type { Doc } from "../convex/_generated/dataModel";
import type { LocalizedRecipeText, RecipeIngredient, RecipeStep } from "../types/recipe";

const SUPPORTED_LANGUAGES: Array<keyof LocalizedRecipeText> = [
  "en",
  "es",
  "zh",
  "fr",
  "ar",
  "ja",
  "vi",
  "tl",
];

const localize = (text: string): LocalizedRecipeText => {
  return SUPPORTED_LANGUAGES.reduce((acc, language) => {
    acc[language] = text;
    return acc;
  }, {} as LocalizedRecipeText);
};

const buildIngredient = (
  code: string,
  quantity: number,
  unit: string,
  preparation?: string,
): RecipeIngredient => {
  const segments = code.split(".");
  const foodCode = segments.slice(0, 3).join(".");
  const varietyCode = segments.length > 3 ? segments.slice(3).join(".") : undefined;

  let preparationValue = preparation;
  if (!preparationValue && varietyCode) {
    if (varietyCode.includes("form.")) {
      preparationValue = varietyCode.split("form.")[1]?.replace(/_/g, " ");
    } else if (varietyCode.includes("state.")) {
      const state = varietyCode.split("state.")[1];
      if (state) {
        preparationValue = state.replace(/_/g, " ");
      }
    }
  }

  return {
    foodCode,
    varietyCode,
    quantity,
    unit,
    preparation: preparationValue,
  };
};

const buildSteps = (steps: string[], times: Array<number | undefined> = []): RecipeStep[] => {
  return steps.map((instruction, index) => ({
    stepNumber: index + 1,
    instructions: localize(instruction),
    timeInMinutes: times[index],
  }));
};

const timestamp = (month: number, day: number) => Date.UTC(2024, month - 1, day);

